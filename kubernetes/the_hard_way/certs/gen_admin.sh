#!/bin/bash

cat > $CONFIG_PATH/ca-config.json <<EOF
{
  "signing": {
    "default": {
      "expiry": "8760h"
    },
    "profiles": {
      "kubernetes": {
        "usages": ["signing", "key encipherment", "server auth", "client auth"],
        "expiry": "8760h"
      }
    }
  }
}
EOF

cat > $CONFIG_PATH/ca-csr.json <<EOF
{
  "CN": "Kubernetes",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "Kubernetes",
      "OU": "CA",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert -initca $CONFIG_PATH/ca-csr.json | cfssljson -bare $CERTS_PATH/ca

cat > $CONFIG_PATH/admin-csr.json <<EOF
{
  "CN": "admin",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:masters",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert \
  -ca=$CERTS_PATH/ca.pem \
  -ca-key=$CERTS_PATH/ca-key.pem \
  -config=$CONFIG_PATH/ca-config.json \
  -profile=kubernetes \
  $CONFIG_PATH/admin-csr.json | cfssljson -bare $CERTS_PATH/admin

